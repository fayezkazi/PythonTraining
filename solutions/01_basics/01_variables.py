# Exercise 1.1: Create variables
name = "Alice"
age = 25
height = 1.68
is_student = True

# Exercise 1.2: Print the variables
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height}m")
print(f"Is student: {is_student}")

# Exercise 1.3: Variable reassignment
age = age + 5
print(f"\nNew age: {age}")

# Exercise 1.4: Type checking
print(f"\nType of name: {type(name)}")
print(f"Type of age: {type(age)}")
print(f"Type of height: {type(height)}")
print(f"Type of is_student: {type(is_student)}")

# Exercise 1.5: String concatenation
message = f"My name is {name} and I am {age} years old"
print(f"\n{message}")
